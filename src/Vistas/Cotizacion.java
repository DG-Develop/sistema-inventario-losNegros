    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int contar = TablaCotizacion.getRowCount();

        String[] Cantidad = new String[contar];
        String[] Descripcion = new String[contar];
        String[] PrecioUnitario = new String[contar];
        String[] Importe = new String[contar];

        for (int i = 0; i < contar; i++) {
            Cantidad[i] = TablaCotizacion.getValueAt(i, 0).toString();
            Descripcion[i] = TablaCotizacion.getValueAt(i, 1).toString();
            PrecioUnitario[i] = TablaCotizacion.getValueAt(i, 2).toString();
            Importe[i] = TablaCotizacion.getValueAt(i, 3).toString();
        }

        con.crearPDFCotizacion(contar, Cantidad, Descripcion, PrecioUnitario, Importe);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        String costo = con.buscarCostoProducto(ComboProductos.getSelectedItem().toString());

        double resultado = Double.parseDouble(costo) * Integer.parseInt(ComboCantidad.getSelectedItem().toString());

        String[] datos = new String[4];
        datos[0] = ComboCantidad.getSelectedItem().toString();
        datos[1] = ComboProductos.getSelectedItem().toString();
        datos[2] = costo;
        datos[3] = String.valueOf(resultado);

        modeloTabla.addRow(datos);

    }//GEN-LAST:event_btnAgregarActionPerformed

